#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*str;
	size_t	i;

	i = 0;
	str = (char *)s;
	while (n > i)
	{
		if (str[i] == c)
			return (str + i);
		i++;
	}
	return (NULL);
}
